package Modelo;


public class ModeloRelatorio {
private int cod_infracao;
private int marcacao;
private int cod_pagamento;
    /**
     * @return the cod_infracao
     */
    public int getCod_infracao() {
        return cod_infracao;
    }

    /**
     * @param cod_infracao the cod_infracao to set
     */
    public void setCod_infracao(int cod_infracao) {
        this.cod_infracao = cod_infracao;
    }

    /**
     * @return the marcacao
     */
    public int getMarcacao() {
        return marcacao;
    }

    /**
     * @param marcacao the marcacao to set
     */
    public void setMarcacao(int marcacao) {
        this.marcacao = marcacao;
    }

    /**
     * @return the cod_pagamento
     */
    public int getCod_pagamento() {
        return cod_pagamento;
    }

    /**
     * @param cod_pagamento the cod_pagamento to set
     */
    public void setCod_pagamento(int cod_pagamento) {
        this.cod_pagamento = cod_pagamento;
    }

    
}
